    private void enterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterActionPerformed
        // Retrieve input values from form fields
        String studno = txt_studno.getText();
        String section = (String) combo_section.getSelectedItem();
        String yrlvl = (String) combo_yrlvl.getSelectedItem();
        String semester = (String) combo_semester.getSelectedItem();
        
        // Get the current date
        Date regDate = new Date();
        SimpleDateFormat df = new SimpleDateFormat("yyyy/MM/dd");
        String date = df.format(regDate);

        //To check if the student exist on the student table and To select the student who recently login
        String studQuery = "SELECT studentNo FROM STUDENT WHERE loginTime = (SELECT TOP 1 loginTime FROM STUDENT ORDER BY loginDate DESC, loginTime DESC)";

        //Insert to Registration table
        String regQuery = "INSERT INTO REGISTRATION VALUES ('"+studno+"','"+section+"','"+date+"')";

        //Checking if the student number already exist
        String rQuery = "SELECT studentNo FROM REGISTRATION WHERE studentNo=?";

        //To insert the log of the student's registration
        String insertLog = "INSERT INTO STUDENT_LOG VALUES('"+studno+"','Successfully registered',default)";
        // SQL query to log the student checking their Certificate of Registration (COR)
         String insertLogIfRegistered = "INSERT INTO STUDENT_LOG VALUES('"+studno+"','Checked their COR',default)";
        
        try{
            studConnect = UserLogins.stud();// Connect to the student database
            pst = studConnect.prepareStatement(studQuery);// Prepare and execute the query to check if the student recently logged in
            rs = pst.executeQuery();
            // Prepare the statement to check if the student number already exists in the REGISTRATION table
            PreparedStatement checkPst = studConnect.prepareStatement(rQuery);

            // If the student number field is empty, show an error message and exit the method
            if(studno.isEmpty()){
                JOptionPane.showMessageDialog(null, "Please fill in all the required fields.");
                return; // Exit the method without proceeding further
            }
            // Set the student number in the check query and execute it
            checkPst.setString(1, studno);
            ResultSet checkRs = checkPst.executeQuery();
            // If the student number already exists, show an error message and log the action
            if(checkRs.next()){
                JOptionPane.showMessageDialog(null, "User already exist");
                PreparedStatement insertlogPST = studConnect.prepareStatement(insertLogIfRegistered);
                insertlogPST.execute();
                PrintSubject(yrlvl,semester);// Print the subject and student details
                printStudentDetails(yrlvl,semester);
                return;

            }
             // If the student exists in the STUDENT table and matches the student number, insert the registration record
            if(rs.next()){
                if(rs.getString("studentNo").equals(studno)){
                    // Insert the registration details into the REGISTRATION table
                    pst = studConnect.prepareStatement(regQuery);
                    pst.execute();
                     // Show a success message
                    JOptionPane.showMessageDialog(null,"Record added successfully");
                    // Log the student's registration
                    PreparedStatement insertlogPST = studConnect.prepareStatement(insertLog);
                    insertlogPST.execute();
                    // Clear the student number field and disable the 'enter' button
                    txt_studno.setText("");
                    enter.disable(); 
                    
                    // Print the subject and student details
                    PrintSubject(yrlvl,semester);
                    printStudentDetails(yrlvl,semester);

                }else{
                     // Show an error message if the student record is not found
                    JOptionPane.showMessageDialog(null, "Student record not found");
                }
            }else{
                
                 // Show an error message if the student record is not found
                JOptionPane.showMessageDialog(null, "Student record not found");
            }
        }catch(Exception e){
            // Show an error message if an exception occurs
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_enterActionPerformed

