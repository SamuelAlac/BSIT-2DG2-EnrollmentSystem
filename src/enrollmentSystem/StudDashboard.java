    // Method to print subjects based on year level and semester
    private void PrintSubject(String yrlvl, String semester){
// SQL query to select relevant subject information for the student
String printCOR = "SELECT cs.subjCode AS 'SUBJECT CODE', sub.subjTitle AS 'SUBJECT TITLE', sub.lec AS 'LEC', sub.lab AS 'LAB', SUM(sub.lec + sub.lab) AS \"CREDIT\", sec.section AS 'SECTION', " +
"emp.fName + ' ' + emp.lName AS 'Employee Name' " +
"FROM REGISTRATION AS reg " +
"INNER JOIN STUDENT AS stud ON reg.StudentNo = stud.StudentNo " +
"INNER JOIN SECTION AS sec ON reg.Section = sec.Section " +
"INNER JOIN COURSE AS crs ON sec.CourseCode = crs.CourseCode " +
"INNER JOIN COLLEGE AS col ON crs.colCode = col.colCode " +
"INNER JOIN COURSESUBJECT AS cs ON sec.Section = cs.Section " +
"INNER JOIN SUBJECT AS sub ON cs.subjCode = sub.subjCode " +
"INNER JOIN EMPLOYEE emp ON sub.empNo = emp.empNo " +
"WHERE reg.studentNo = (SELECT TOP 1 studentNo FROM STUDENT ORDER BY loginDate DESC, loginTime DESC) AND cs.YrLevel = '"+yrlvl+"' AND cs.semester = '"+semester+"' " +
"GROUP BY reg.registrationNo, stud.studentNo, stud.fName, stud.lName, stud.bDate, stud.gender, col.colName, crs.courseDesc, cs.yrlevel, cs.semester, cs.subjCode, sub.subjTitle, sub.lec, sub.lab, sec.section, stud.loginDate, stud.loginTime, emp.fname, emp.lname " +
"ORDER BY stud.loginDate DESC, stud.loginTime DESC";
            try{
                
            adminConnect = UserLogins.admin();// Connect to the admin database
            st = studConnect.createStatement();// Create a statement object to execute the query
            rs = st.executeQuery(printCOR); // Execute the query and get the result set
            enrolledSubject.setModel(DbUtils.resultSetToTableModel(rs));// Set the model of the enrolledSubject table to display the result set
            DefaultTableModel model = (DefaultTableModel) enrolledSubject.getModel();// Get the table model
            // Loop through each row in the table and extract the data
            for(int i = 0; i < enrolledSubject.getRowCount(); i++){
                 // Get each value from the row
                String subcode = enrolledSubject.getValueAt(i, 0).toString();
                String subtitle = enrolledSubject.getValueAt(i, 1).toString();
                String lec = enrolledSubject.getValueAt(i, 2).toString();
                String lab = enrolledSubject.getValueAt(i, 3).toString();
                String credit = enrolledSubject.getValueAt(i, 4).toString();
                String section = enrolledSubject.getValueAt(i, 5).toString();
                String emp = enrolledSubject.getValueAt(i, 6).toString();
                subjcode.setText(subjcode.getText()+subcode+"\n");
                subjtitle.setText(subjtitle.getText()+subtitle+"\n");
                lecture.setText(lecture.getText()+lec+"\n");
                laboratory.setText(laboratory.getText()+lab+"\n");
                credits.setText(credits.getText()+credit+"\n");
                sections.setText(sections.getText()+section+"\n");
                employee.setText(employee.getText()+emp+"\n");
                
            }
                    
            }catch(Exception e){
                 // Show an error message dialog if an exception occurs
                JOptionPane.showMessageDialog(null, e);
        }
    }
