    private void printCorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printCorActionPerformed
        //To Print Certificate of Registration
        PrinterJob job = PrinterJob.getPrinterJob();// Create a PrinterJob instance
        job.setJobName("Print Data");// Set the name of the print job

        // Set the Printable object for the print job
        job.setPrintable(new Printable() {
            @Override
            public int print(Graphics graphics, PageFormat pf, int pageNum) throws PrinterException {
                if(pageNum > 0){
                     // Check if the page number is greater than 0 (only one page to print)
                    return Printable.NO_SUCH_PAGE;// No such page to print
                }
                // Cast the Graphics object to Graphics2D for more control
                Graphics2D g2 = (Graphics2D)graphics;
                g2.translate(pf.getImageableX(), pf.getImageableY());// Translate the graphics to the imageable area of the page
                g2.scale(0.450,0.900);// Scale the graphics to fit the content to the page

                // Paint the content (certificate) onto the Graphics2D object
                certificate.paint(g2);
                return Printable.PAGE_EXISTS;// Indicate that the page exists and was rendered

            }
        });
        try{
            //To print the document
            job.print();
        }catch(PrinterException e){
            // If a PrinterException occurs, show a message dialog with the error
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_printCorActionPerformed

